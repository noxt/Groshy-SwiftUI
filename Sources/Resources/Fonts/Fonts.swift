import SwiftUI


extension Font {

    struct Rubik {

        static func MediumItalic(size: CGFloat) -> Font {
            return Font.custom("Rubik-MediumItalic", size: size)
        }

        static func Bold(size: CGFloat) -> Font {
            return Font.custom("Rubik-Bold", size: size)
        }

        static func Light(size: CGFloat) -> Font {
            return Font.custom("Rubik-Light", size: size)
        }

        static func Medium(size: CGFloat) -> Font {
            return Font.custom("Rubik-Medium", size: size)
        }

        static func Black(size: CGFloat) -> Font {
            return Font.custom("Rubik-Black", size: size)
        }

        static func Italic(size: CGFloat) -> Font {
            return Font.custom("Rubik-Italic", size: size)
        }

        static func LightItalic(size: CGFloat) -> Font {
            return Font.custom("Rubik-LightItalic", size: size)
        }

        static func BlackItalic(size: CGFloat) -> Font {
            return Font.custom("Rubik-BlackItalic", size: size)
        }

        static func Regular(size: CGFloat) -> Font {
            return Font.custom("Rubik-Regular", size: size)
        }

        static func BoldItalic(size: CGFloat) -> Font {
            return Font.custom("Rubik-BoldItalic", size: size)
        }

    }

}
